    private void lanzarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lanzarActionPerformed
        new JFXPanel();
        String dd = new File("dados.mp3").toURI().toString();
        new MediaPlayer(new Media(dd)).play();

        int d1 = (int) (Math.random() * 6) + 1;
        int d2 = (int) (Math.random() * 6) + 1;
        int result = d1 + d2;
        for (int k = 0; k < players; k++) {
            if (result == resultados[k]) {
                while (result == resultados[k]) {
                    d1 = (int) (Math.random() * 6) + 1;
                    d2 = (int) (Math.random() * 6) + 1;
                    result = d1 + d2;
                }

            }

        }

        int may = 0;

        Icon uno = new javax.swing.ImageIcon(getClass().getResource("/imagenes/cara1.png"));
        Icon dos = new javax.swing.ImageIcon(getClass().getResource("/imagenes/cara2.png"));
        Icon tres = new javax.swing.ImageIcon(getClass().getResource("/imagenes/cara3.png"));
        Icon cuatro = new javax.swing.ImageIcon(getClass().getResource("/imagenes/cara4.png"));
        Icon cinco = new javax.swing.ImageIcon(getClass().getResource("/imagenes/cara5.png"));
        Icon seis = new javax.swing.ImageIcon(getClass().getResource("/imagenes/cara6.png"));

        switch (turnoact) {
            case 1:
                if (!"".equals(Inicio.J1Name.getText())) {
                    turnode.setText("Dados para " + Inicio.J1Name.getText());
                } else {
                    turnode.setText("Dados para P1");
                }
                break;
            case 2:
                if (!"".equals(Inicio.J2Name.getText())) {
                    turnode.setText("Dados para " + Inicio.J2Name.getText());
                } else {
                    turnode.setText("Dados para P2");
                }
                break;
            case 3:
                if (!"".equals(Inicio.J3Name.getText())) {
                    turnode.setText("Dados para " + Inicio.J3Name.getText());
                } else {
                    turnode.setText("Dados para P3");
                }
                break;
            case 4:
                if (!"".equals(Inicio.J4Name.getText())) {
                    turnode.setText("Dados para " + Inicio.J4Name.getText());
                } else {
                    turnode.setText("Dados para P1");
                }
                    ;
                break;
        }

        switch (d1) {
            case 1:
                dado1.setIcon(uno);
                break;
            case 2:
                dado1.setIcon(dos);
                break;
            case 3:
                dado1.setIcon(tres);
                break;
            case 4:
                dado1.setIcon(cuatro);
                break;
            case 5:
                dado1.setIcon(cinco);
                break;
            case 6:
                dado1.setIcon(seis);
                break;
        }
        switch (d2) {
            case 1:
                dado2.setIcon(uno);
                break;
            case 2:
                dado2.setIcon(dos);
                break;
            case 3:
                dado2.setIcon(tres);
                break;
            case 4:
                dado2.setIcon(cuatro);
                break;
            case 5:
                dado2.setIcon(cinco);
                break;
            case 6:
                dado2.setIcon(seis);
                break;
        }

        switch (turnoact) {
            case 1:
                result1.setText(result + "");
                break;
            case 2:
                result2.setText(result + "");
                break;
            case 3:
                result3.setText(result + "");
                break;
            case 4:
                result4.setText(result + "");

                break;
        }
        turnoact++;
        resultados[i] = result;
        comparacion[i] = result;
        i++;
        if (turnoact > players) {
            lanzar.setEnabled(false);
            iniciar.setEnabled(true);
            int j, aux;
            for (int k = 0; k < comparacion.length - 1; k++) {
                for (j = 0; j < comparacion.length - k - 1; j++) {
                    if (comparacion[j + 1] > comparacion[j]) {
                        aux = comparacion[j + 1];
                        comparacion[j + 1] = comparacion[j];
                        comparacion[j] = aux;
                    }
                }
            }

            for (int p = 0; p < resultados.length; p++) {
                for (j = 0; j < comparacion.length; j++) {
                    if (resultados[p] == comparacion[j]) {
                        turno[p] = j + 1;

                    }
                }

            }
            turnos.setText("Turno para " + Inicio.J1Name.getText() + " : " + turno[0]);
            turnos.setText(turnos.getText() + "\nTurno para " + Inicio.J2Name.getText() + " : " + turno[1]);
            if (players == 3) {
                turnos.setText(turnos.getText() + "\nTurno para " + Inicio.J3Name.getText() + " : " + turno[2]);
            } else if (players == 4) {
                turnos.setText(turnos.getText() + "\nTurno para " + Inicio.J3Name.getText() + " : " + turno[2]);
                turnos.setText(turnos.getText() + "\nTurno para " + Inicio.J4Name.getText() + " : " + turno[3]);
            }
        }

    }//GEN-LAST:event_lanzarActionPerformed

